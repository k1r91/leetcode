from typing import List


class Solution:

    def arrayPairSumBrainfuck(self, nums: List[int]) -> int:
        exist = [0] * 20001
        for i in range(len(nums)):
            exist[nums[i] + 10000] += 1
        odd = True
        sum = 0
        i = 0
        for i in range(len(exist)):
            while exist[i] > 0:
                if odd:
                    sum += i - 10000
                odd = not odd
                exist[i] -= 1
        return sum

    def arrayPairSum(self, nums: List[int]) -> int:
        nums.sort()
        return sum(nums[::2])
        # return sum(sorted(nums)[::2])
        # native more fastest than sum from sorted
        nums = sorted(nums)
        _sum = 0
        for i, num in enumerate(nums):
            if i % 2 == 0:
                _sum += num
        return _sum


